export function isZero(n) {
    // Return true if n is zero
    return n === 0;
}
export function lessThan(a, b) {
    // Return true if a < b
    return a < b;
}
export function lessThanOrEqual(a, b) {
    // Return true if a <= b
    return a <= b;
}
export function greaterThan(a, b) {
    // Return true if a > b
    return a > b;
}
export function greaterThanOrEqual(a, b) {
    // Return true if a >= b
    return a >= b;
}
export function areBothZero(a, b) {
    // Return true if both a and b are zero
    return isZero(a) && isZero(b);
}
